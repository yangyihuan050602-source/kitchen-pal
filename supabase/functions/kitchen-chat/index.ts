import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

// config.toml entry needed for this function is already present

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are a friendly, knowledgeable Kitchen AI cooking assistant. You help users cook delicious meals with what they have.

Your personality:
- Warm, encouraging, and practical
- Give concise but helpful answers
- Use cooking emojis naturally 🍳🥘🍝
- Always suggest alternatives when an ingredient is missing

Context about the user's kitchen:
- Available ingredients: ${context?.inventory?.join(", ") || "not specified"}
${context?.selectedRecipe ? `- Currently cooking: ${context.selectedRecipe.name}
- Recipe steps: ${context.selectedRecipe.steps?.map((s: string, i: number) => `${i + 1}. ${s}`).join("\n")}
- Tips: ${context.selectedRecipe.tips?.join(", ") || "none"}` : "- No recipe selected yet"}

Guidelines:
- If asked about substitutions, give practical alternatives with ratios
- If asked about cooking times/temperatures, be specific
- If the user has expiring ingredients, suggest using them first
- Keep answers concise (2-4 sentences for simple questions)
- For step-by-step guidance, be more detailed`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [{ role: "system", content: systemPrompt }, ...messages],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Too many requests. Please wait a moment and try again." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please add credits." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
